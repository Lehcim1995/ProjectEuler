    private bool PointInTriangle (float ptx, float pty, float v1x, float v1y, float v2x, float v2y, float v3x, float v3y)
    {
        float d1, d2, d3;
        bool has_neg, has_pos;

        d1 = sign(ptx, pty,  v1x, v1y, v2x, v2y);
        d2 = sign(ptx, pty,  v2x, v2y, v3x, v3y);
        d3 = sign(ptx, pty,  v3x, v3y, v1x, v1y);

        has_neg = (d1 < 0) || (d2 < 0) || (d3 < 0);
        has_pos = (d1 > 0) || (d2 > 0) || (d3 > 0);

        return !(has_neg && has_pos);
    }

